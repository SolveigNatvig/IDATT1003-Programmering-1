

public class HelloWorld {

    public static void main(String args[]) {
        final double LENGHT = 5.0; // desimaltall variabel som ikke kan endres (final)
        double width = 2.3; // Vanlig desimaltall variabel (kan man bruke float i stedet?)
        double area = LENGHT * width;
        System.out.println("The area is " + area + "square metres");
        System.out.println("Hello World!");

        System.out.println("Hi");
    
    }
}

